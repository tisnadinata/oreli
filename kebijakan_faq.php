		<blockquote>
			<p>4. Apakah sistem pemasaran ORELI sama dengan MLM (Multi Level Marketing) ?</p>
			<footer>
				ORELI adalah E-commerce dengan system pemasaran berjaringan satu level. ORELI menawarkan peluang bisnis dengan bergabung menjadi member atau agen. Member atau agen akan mengembangkan bisnisnya dengan mengajak dan mensponsori calon member lain bergabung. Seorang member atau agen beserta member-member yang disponsorinya secara langsung adalah merupakan satu grup jaringan ORELI. seorang member atau agen akan mendapatkan bonus dihitung berdasarkan total poin yang dikumpulkan dalam satu grup jaringan miliknya. Poin berasal dari setiap pembelian baik oleh member itu sendiri maupun member yang disponsorinya. Jika saat ini anda menjalankan bisnis MLM, jangan  dibandingkan dengan system pemasaran kami. Di ORELI, Anda memiliki peluang bisnis dan kesempatan mendapatkan bonus berupa pasif income dan bonus tahunan  berupa Logam Mulia, jalan-jalan ke luar negeri bahkan mobil dengan bergabung menjadi member atau agen.
			</footer>
		</blockquote>
		<blockquote>
			<p>5. Jika seorang member A mensponsori member B, kemudian B mensposori C, apakah A berarti mensponsori C? Jika C melakukan pembelian apakah A ikut mendapatkan poin bonus ?</p>
			<footer>
				C tidak memiliki garis sponsor dengan A sehingga jika C melakukan pembelian maka A tidak mendapatkan poin bonus. Poin Bonus sponsor akan diperoleh oleh B. 
			</footer>
		</blockquote>
		<blockquote>
			<p>6. Siapa yang bisa menjadi member? Bagaiman persyaratan menjadi member ?</p>
			<footer>
				Siapapun Warga Negara Indonesia yang telah berusia minimal 17 (tujuh belas) tahun dan memiliki KTP bisa menjadi member ORELI. Untuk menjadi member, calon member harus mengisi data secara lengkap di web dan selanjutnya harus melakukan pembelajaan 2 (dua) jenis barang kombinasi atasan atau bawahan yang ada di website ORELI. Barang yang dibeli ini dapat digunakan untuk mengenalkan produk ORELI pada calon member yang ingin bergabung. 
			</footer>
		</blockquote>
		<blockquote>
			<p>7. Apa Beda Member dengan Agen ?</p>
			<footer>
				Member dan Agen adalah sama-sama merupakan anggota ORELI. Perbedaannya terletak pada fasilitas yang diberikan kepada keduanya. Member mendapatkan diskon pembelian sebesar 20% sedangkan Agen akan mendapatkan diskon sebesar 30%. Seorang agen merupakan representasi Toko Offline ORELI. Untuk menjadi agen seseorang harus harus mendaftar menjadi member terlebih dahulu kemudian mengirimkan email kepada customer@oreli.co.id perihal permohonan menjadi agen. Seorang agen harus melakukan pembelanjaan minimal Rp20.000.000,- (dua puluh juta) sebelum diskon pada saat setiap kali berbelanja. 
			</footer>
		</blockquote>
		<blockquote>
			<p>8. Apakah keanggotaan ORELI bisa hangus ?</p>
			<footer>
				Tidak, Keanggotaan ORELI tidak hangus. Sekali mendaftar, keanggotaan ORELI berlaku seumur hidup. 
			</footer>

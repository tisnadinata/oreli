		<blockquote>
			<p>9. Bagaimana perhitungan bonus di ORELI?</p>
			<footer>
				Bonus di ORELI terdiri dari bonus Bulanan dan Bonus tahunan. Bonus diberikan atas poin yang diakumulasi oleh member atau agen berupa jumlah poin pembelian pribadi ditambah jumlah poin sponsor selama 1 (satu) bulan disebut bonus bulanan dan 1 (satu) tahun disebut bonus tahunan. Bonus bulanan diberikan berupa uang tunai dengan jumlah per 1 (satu) poin bernilai Rp1000,- (seribu rupiah). Bonus tahunan diberikan berdasarkan jumlah tertentu akumulasi poin pribadi ditambah jumlah poin sponsor selama 1 (satu) tahun yang nilainya ditetapkan oleh perusahaan.Bonus tahunan diberikan berupa Logam mulia, Bonus jalan jalan ke luar negeri, bahkan Mobil!
			</footer>
		</blockquote>

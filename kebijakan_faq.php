			<footer>
				Pembayaran pesanan dilakukan melalui transfer ke rekening PT ORELI di Bank yang ditentukan atau menggunakan sarana lainnya yang disediakan dan ditetapkan oleh ORELI. Saat ini pembayaran dapat dilakukan melalui transfer, kartu kredit dan melalui Alfamart. 

			<p>15. Apakah setelah melakukan pembayaran saya perlu konfirmasi manual?</p>
			<footer>
				Pembayaran harus sesuai dengan jumlah berdasarkan tagihan (Invoice). Jika jumlah yang ditransfer sudah sesuai dengan invoice, Anda tidak perlu melakukan konfirmasi pembayaran. Jika jumlah pembayaran anda tidak sesuai dengan Invoice, maka anda harus melakukan konfirmasi pembayaran manual di www.oreli.co.id. Jika tidak melakukan konfirmasi manual, maka pesanan anda diproses menunggu konfirmasi manual. 

			<p>1. Apa itu ORELI ?</p>
			<footer>
				ORELI adalah singkatan dari PT Toko Ritel Indonesia, Badan hukum yang didirikan di Indonesia yang bertujuan melakukan usaha perdagangan online atau E-Commerce.  ORELI fokus pada perdangangan produk-produk fashion yang berkualitas dengan harga yang pas di kantong. Saat ini, ORELI memasarkan produk fashion dengan merk Estee, merk Indonesia dengan kualitas terjamin terutama pakaian berbahan dasar Denim. Pabrik kami memiliki  kapasitas dan kapabiltas memproduksi produk setara merk ternama internasional, dengan bahan dan standar kualitas yang tinggi dan terjamin.
			</footer>
		</blockquote>
		<blockquote>
			<p>2. Apakah ORELI hanya menjual produk pakaian ?</p>
			<footer>
				Saat ini ORELI menjual pakaian atasan dan bawahan baik pria dan wanita dengan merk Estee, spesialis pakaian berbahan denim/Jeans. Kami akan terus menghadirkan dan menambah portofolio produk berkualitas lainnya kepada anda tidak  terbatas pada pakaian saja. Saat ini tim creative kami sedang mempersiapkan produk berkualitas lainnya baik sepatu, tas, dan aksesoris lainnya, tentunya dengan standar kualitas ORELI yang terjamin. 
			</footer>
		</blockquote>
		<blockquote>
			<p>3. Apakah ORELI memiliki toko offline ?</p>
			<footer>
				ORELI tidak memiliki toko offline di bawah manajemen langsung ORELI. Toko offline ORELI adalah milik agen ORELI.
			</footer>
